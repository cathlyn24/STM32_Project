int main(void)
{
    DHT11_Data sensor_data;
    char lcd_buffer[17];
    char uart_buffer[64];
    uint8_t read_status;

  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_TIM2_Init();
  HAL_TIM_Base_Start(&htim2);  // ADD THIS LINE MANUALLY
  MX_USART1_UART_Init();
  MX_I2C1_Init();

  /* Initialize LCD */
    LCD_Init(&hi2c1);
    LCD_Clear();
    LCD_SetCursor(0, 0);
    LCD_Print("Weather Station");
    LCD_SetCursor(1, 0);
    LCD_Print("Initializing...");
    HAL_Delay(2000);

    /* Initialize DHT11 */
        DHT11_Init(GPIOA, GPIO_PIN_1, &htim2);

        LCD_Clear();
        /* Send welcome message via UART */
            char *welcome = "Weather Station Started\r\n";
            HAL_UART_Transmit(&huart1, (uint8_t*)welcome, strlen(welcome), 100);

            /* Infinite loop */
            while (1)
            {
                /* Read DHT11 sensor */
                read_status = DHT11_Read(&sensor_data);

                if(read_status == DHT11_OK) {
                    /* Display on LCD - Line 1: Temperature */
                    LCD_SetCursor(0, 0);
                    sprintf(lcd_buffer, "Temp: %d C      ", (int)sensor_data.temperature);
                    LCD_Print(lcd_buffer);

                    /* Display on LCD - Line 2: Humidity */
                    LCD_SetCursor(1, 0);
                    sprintf(lcd_buffer, "Hum:  %d %%      ", (int)sensor_data.humidity);
                    LCD_Print(lcd_buffer);

                    /* Send data via UART */
                    sprintf(uart_buffer, "Temperature: %d C, Humidity: %d %%\r\n",
                            (int)sensor_data.temperature,
                            (int)sensor_data.humidity);
                    HAL_UART_Transmit(&huart1, (uint8_t*)uart_buffer, strlen(uart_buffer), 100);
                }
                else {
                    /* Display error on LCD */
                    LCD_SetCursor(0, 0);
                    LCD_Print("Sensor Error!   ");
                    LCD_SetCursor(1, 0);
                    LCD_Print("Check Wiring    ");

                    /* Send error via UART */
                    char *error_msg = "DHT11 Read Error\r\n";
                    HAL_UART_Transmit(&huart1, (uint8_t*)error_msg, strlen(error_msg), 100);
                }

                /* Wait 2 seconds before next reading (DHT11 needs at least 1 sec) */
                HAL_Delay(2000);
            }
        }

